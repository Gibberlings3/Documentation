      <h2>Romance</h2>
      <p>Aran will romance any female protagonist of any race save Drow, or any alignment. Again, how he reacts will definitely let the player understand his position. Good men fall for bad women as quickly as for good ones, and sometimes opposites really do attract. But romance on the trail is awkward, with a group of adventurers around, and the road to romance is both rocky and complicated. Exactly how does a strong man feel when his significant other turns out to be infinitely stronger than he, on so many levels? Can the two of you negotiate the attraction into something of an equal relationship, or is this destined to fail? For a sample of lovetalks, please visit the forums.</p>
      <p>When Aran Whitehand meets the party, he is looking for work having dropped from the rolls of a mercenary regiment. </p>
