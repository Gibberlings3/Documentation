      <h2> The Mod Concept</h2>
        NPCs work in many different ways, based primarily on the original BioWare model. What happens, though, if we try a more 'self aware' NPC, allowing player choice for some of the fundamentals? Is an NPC's character his class and stats, or is it really a personality? Can an NPC still be true to himself, even when a player chooses to turn them into a new class combination? This mod explores the idea that jobs may come and go, and in the heart of an accountant may lie a lion, even if the body doesn't quite show it off. The NPC should be able to adapt to being a new class or kit combination, while retaining his core personality. As a side project, the intent is to post code in the forum to eventually tie to tutorials and provide insight into the decision-making process when turning an idea into functional code.</p>
      <p> Aran Whitehand is functionally a Companion mod. He has a minor optional quest with a new area, but comes with no special weapons or upgradeable items - his specialty is in interacting with the members of the party and adapting to situations. For him, the job is paramount, and his job is supporting the player. He is no stranger to having to carry out orders which bend morality, though he hates doing so. It should be noted that this mod is designed by adults for adults, and should not be installed unless you are comfortable with Faerunian swearing, occasional adult themes, and NC - 17 to R rated content in the Romance portion.</p>
      <h2> The Companion NPC </h2>
        <strong> Aran Whitehand </strong> is a human fighter and scribe for BG2, dual classable, so that he can be Tinker, Tailor, Soldier, Spy... whatever your party needs. Basically, a human utility player taken along for the friendship/cuddling. "Specials" include creation of scrolls through dialog and timer when in a party with another canonical mage. Primarily a male friend who can grow into an extended courtship/romance, romanceable by female PCs, Aran has his own sense of humor and specializes in bringing others into the conversation. </p>
